         url = url.substring(0, (url.indexOf("#") == -1) ? url.length : url.indexOf("#"));
         url = url.substring(0, (url.indexOf("?") == -1) ? url.length : url.indexOf("?"));
         url = url.substr(url.lastIndexOf("/") + 1);
         if(url == ''){
          url = 'index.php';
         }
         $('nav ul li').each(function(){
          var href = $(this).find('a').attr('href');
          if(url == href){
           $(this).addClass('active');
           $(this).parents().andSelf().addClass('active');
          }
         });
        });    
